package July_20;
import java.util.*;
public class Positive_or_Negative {

	public static void main(String[] args) {
		int a;
		Scanner sc= new Scanner(System.in);
		a=sc.nextInt();
		if(a>=0)
		{
			System.out.println(a+" is positive");
		}
		else
		{
			System.out.println(a+ " is negative");
		}
	}

}
